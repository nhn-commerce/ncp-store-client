2) 타인의 정보도용
3) 회사의 임직원, 운영자, 기타 관계자를 사칭하는 행위
4) 회사가 게시한 정보를 변경하는 행위
5) 회사가 금지한 정보(컴퓨터 프로그램 등) 등의 송신 또는 게시 행위
6) 회사가 제공 또는 승인하지 않은 컴퓨터 프로그램이나 기기 또는 장치를 제작, 배포, 이용, 광고하는 행위
