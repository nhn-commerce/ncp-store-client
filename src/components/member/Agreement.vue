9) 외설 또는 폭력적인 메시지, 화상, 음성, 기타 공서양속에 반하는 정보를 회사에 공개 또는 게시하는 행위
10) 회사의 동의 없이 영리, 영업, 광고, 정치활동 등을 목적으로 서비스를 이용하는 행위
11) 기타 관계 법령에서 금지하거나 선량한 풍속 기타 사회통념상 허용되지 않거나 부당한 행위</span>
2. 이용자는 관계 법령, 이 약관의 규정, 이용안내 및 서비스와 관련하여 공지한 주의사항, 회사가 통지하는 사항 등을 확인하고 준수하여야 하며, 기타 회사의 업무에 방해되는 행위를 하여서는 안 됩니다.


<strong>제15조 이용자의 서비스 이용</strong>

1. 이용자는 스스로 책임지고 상품을 구매하여야 하고, 상품을 구매하기 전에 반드시 회사 또는 제휴사가 서비스 내에 작성한 상품의 상세 내용과 거래의 조건을 정확하게 확인해야 합니다. 구매하려는 상품의 내용과 거래의 조건을 확인하지 않고 구매하여 발생한 모든 손실과 손해는 이용자 본인이 부담합니다.
2. 이용자는 구매의사 없이 회사 또는 제휴사의 판매상품에 구매의사를 표시하여서는 아니 되고, 회사 또는 제휴사의 상품판매기회를 방해하거나 다른 이용자의 구매기회를 방해하여서는 안 됩니다.
3. 이용자의 구매 신청 내용에 허위, 누락, 오기가 있는 경우 혹은 미성년자가 담배, 주류 등 청소년보호법에서 금지하는 상품을 구매하는 것과 같이 이용자가 관련 법률에 위반되는 구매신청을 하는 경우, 회사는 해당 구매신청을 승낙하지 아니하거나 이를 사후에 알게 되는 경우 구매신청을 취소할 수 있습니다.
4. 이용자는 이 약관 및 회사가 서비스 화면에서 공지하는 내용을 준수하여야 하고, 이 약관 및 공지 내용을 위반하거나 이행하지 않아서 발생하는 모든 손실과 손해에 대하여 책임을 집니다.
5. 이용자는 구매한 상품에 청약철회의 원인이 발생하면 수령한 상품을 임의로 사용하거나 훼손되도록 방치해서는 아니 되고, 청약철회 상품의 임의사용이나 상품보관의 미흡으로 상품이 훼손되었을 때에는 그에 합당한 비용을 부담하여야 합니다.
6. 이용자는 회사 또는 제휴사와의 상품매매 절차에서 분쟁이 발생하면 분쟁의 해결을 위하여 성실히 임해야 하며, 분쟁해결의 불성실로 회사 또는 제휴사에 손실이나 손해가 발생하면 그에 대한 모든 책임을 부담하여야 합니다.
7. 이용자는 엔에이치엔페이코㈜가 제공하는 PAYCO 결제시스템을 이용하여 구매할 수 있습니다. 이용자가 상품을 구매할 경우에는 본인 명의의 결제수단을 사용하여야 하고, 타인의 결제수단을 임의로 사용하여서는 아니 되며, 타인의 결제수단을 임의로 사용하여 회사, 제휴사 및 해당 결제수단 소유자에게 발생하는 손실 또는 손해 등에 대한 모든 책임은 이용자가 부담합니다.
8. 매매대금의 결제와 관련하여 이용자가 입력한 정보 및 그 정보와 관련하여 발생하는 책임과 불이익은 전적으로 이용자가 부담합니다.
9. 회사는 이용자가 상품을 주문한 후 24시간 이내에 매매대금을 결제하지 아니하는 경우에는 해당 주문을 취소한 것으로 간주하여 이를 자동으로 취소할 수 있습니다.
10. 회사는 이용자의 매매대금 결제 시 해당 결제수단에 대하여 정당한 사용권한이 있는지 확인할 수 있고, 이에 대한 확인이 완료될 때까지 해당 거래의 진행을 중지하거나 해당 거래를 취소할 수 있습니다.
11. 회사는 이용자에게 서비스가 안전하게 제공될 수 있도록 각종 설비와 자료를 관리해야 하며, 서비스가 제공 목적에 맞게 이용되고 있는지 확인하고, 이용 목적에 위반되는 부분이 있는 것으로 확인되면 사유의 소명을 요청할 수 있습니다.
12. 회사가 이용자의 서비스의 이용 편의를 높이기 위해 제휴사 등으로부터 상품 관련 정보를 제공받아 게재하거나 제3자가 제공하는 방식으로 서비스를 통하여 참조용 상품정보나 관련 콘텐츠를 제공하는 경우에도 이용자는 상품 구매와 관련하여 자신의 판단과 책임으로 결정하여야 하고, 회사는 어떠한 경우에도 이용자의 구매 결정에 대하여 책임을 지지 않습니다.
13. 회사는 회사가 판매하는 상품이 품절 등의 사유로 이용자에게 제공이 불가한 경우, 이와 같은 사실을 현재의 상품 정보를 게시한 곳에 즉시 공지하고, 회사가 제공하기로 이용자와 계약을 체결한 상품에 관하여 품절 등의 사유가 발생한 경우 이를 이용자에게 즉시 통지합니다.
14. 이용자는 제6조에 따른 회원 가입 없이 서비스를 이용할 수 있고 비회원 구매가 가능하지만, 회원 전용 서비스와 혜택(쿠폰, 할인 등)은 비회원 구매 시 적용되지 않을 수 있습니다.
15. 특정 상품(회원 혜택을 위한 특가 상품, 성인용 상품 등)은 제6조에 따른 회원 가입이 필요하며, 회원 가입 절차 없이 해당 상품의 구매는 불가능 합니다.


<strong>제16조 결제대금 예치서비스의 이용</strong>

1. 회사는 서비스에서 제공하는 전자상거래 서비스의 신뢰성을 제고하고 안전한 거래가 이루어질 수 있도록 결제대금 예치서비스를 제공합니다.
