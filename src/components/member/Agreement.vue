1. 회사는 “정보통신망 이용촉진 및 정보보호 등에 관한 법률” 등 관계 법령이 정하는 바에 따라 이용자의 개인정보를 보호하기 위해 노력합니다. 이용자 개인정보의 보호 및 사용에 대해서는 관계 법령 및 회사의 개인정보처리방침이 적용됩니다.
2. 회사의 서비스 이외의 단순히 링크된 제3자의 사이트에서는 회사의 개인정보처리방침이 적용되지 않습니다. 링크된 사이트 및 상품을 제공하는 제3자의 개인정보 처리와 관련하여서는 해당 사이트 및 제3자의 개인정보처리방침을 확인할 책임은 이용자에게 있으며, 회사는 이에 대하여 책임을 부담하지 않습니다.
3. 회사는 이용자의 귀책사유로 인하여 노출된 이용자의 계정정보를 포함한 모든 정보에 대해서 일체의 책임을 지지 않습니다.
4. 회사가 제공하는 서비스를 통하여 주문 및 결제가 이루어진 경우 거래 당사자 간 원활한 의사소통 및 배송, 상담 등 거래 이행을 위하여 관련된 정보를 관계 법령을 준수하는 범위에서 거래 당사자에게 제공할 수 있습니다.


<strong>제10조 회원 탈퇴 등</strong>

1. 회원은 회사에 언제든지 탈퇴를 요청할 수 있으며, 회사는 관계 법령 등이 정하는 바에 따라 이를 즉시 처리하여야 합니다. 다만, 회사는 회원의 회원가입 후 일정시간 동안 서비스 부정이용 방지 등의 사유로 즉시 탈퇴를 제한할 수 있습니다.
2. 회원은 탈퇴를 요청하기 전에 현재 진행 중인 모든 상품의 거래를 완료하거나 철회 또는 취소하여야 하며, 거래의 철회 또는 취소로 인한 불이익은 회원 본인이 부담하여야 합니다.
3. 본 조 제1항에 따라 회원이 회원 탈퇴를 할 경우 회사는 관계 법령에 따라 회원정보를 보유해야 하는 경우를 제외하고는 즉시 회원의 개인정보를 삭제 합니다.
4. 회원의 회원탈퇴, 자격 상실 등으로 본 약관의 적용이 종료되는 경우 회사가 회원에게 제공하였던 쿠폰, 할인 등의 혜택은 모두 자동소멸되고, 재가입시에도 이는 복원되지 않습니다.


<strong>제11조 이용제한 등</strong>

1. 이용자가 다음 각 호의 어느 하나에 해당하는 경우, 회사는 회원자격을 제한, 정지시키거나 이용계약을 해지할 수 있습니다.
<span class="sublst">1) 이 약관의 의무를 위배하거나 서비스의 정상적인 운영을 방해한 경우
2) 가입 신청 시에 허위 내용을 등록한 경우
3) 서비스를 이용하여 구입한 상품의 대금, 기타 서비스 이용과 관련하여 이용자가 부담하는 채무를 기일에 지급하지 않는 경우
4) 다른 사람의 서비스 이용을 방해하거나 그 정보를 도용하는 등 전자상거래 질서를 위협하는 경우
5) 서비스를 이용하여 법령 또는 이 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우
6) 기타 회사가 합리적인 판단에 따라 서비스의 제공을 거부할 필요가 있다고 인정하는 경우</span>
2. 회사가 이용계약을 해지하는 경우, 해지사유와 해지일을 제12조에 따라 이용자에게 통지합니다.
3. 회사의 이용제한, 해지에 정당한 사유가 있는 경우에 회사는 이용제한, 해지로 인하여 이용자가 입은 손해를 배상하지 않습니다.
4. 회사는 “정보통신망 이용촉진 및 정보보호 등에 관한 법률” 및 동법 시행령에 따라 1년간 서비스를 이용하지 않는 회원의 개인정보를 보호하기 위해 개인정보 파기 등 필요한 조치를 취합니다. 또한, 회사는 객관적으로 계정정보 도용 피해가 우려되는 경우에는 회원정보의 보호 및 운영의 효율성을 위해 임시조치, 이용제한, 계정정보 삭제 등 필요한 조치를 취할 수 있습니다. 본 항에 따른 조치가 취해지는 경우, 회사는 조치일 30일 전까지 개인정보 파기 등 필요한 조치가 취해진다는 사실, 일시 및 항목을 명시하여 제12조에 따라 통지합니다.
5. 본 조에 따라 이용계약이 해지되는 경우 회원의 개인정보에 관하여 회사는 제10조 제(3)항에 따라 처리합니다.
6. 회원은 본 조에 따른 이용제한 등에 대해 회사가 정한 절차에 따라 이의신청을 할 수 있으며, 이 때 이의가 정당하다고 회사가 인정하는 경우 회사는 즉시 서비스의 이용을 재개합니다.


<strong>제12조 이용자에 대한 통지</strong>

1. 회사는 불특정다수 이용자에 대한 통지의 경우 1주일 이상 서비스 화면에 게시함으로써 개별통지에 갈음할 수 있습니다.
2. 회사가 특정 이용자 본인의 거래와 관련하여 중대한 영향을 미치는 사항에 대하여는 통지를 하는 경우, 회원가입 시 회사와 미리 약정하여 지정한 전자우편 등으로 개별 통지합니다.


<strong>제13조 회사의 의무</strong>

1. 회사는 관계 법령과 이 약관이 금지하거나 미풍양속에 반하는 행위를 하지 않으며, 계속적이고 안정적으로 서비스를 제공하기 위하여 최선을 다하여 노력합니다.
2. 회사는 이용자가 안전하게 서비스를 이용할 수 있도록 개인정보(신용정보 포함)보호를 위해 보안시스템을 갖추어야 하며 개인정보처리방침을 공시하고 준수합니다. 회사는 이 약관 및 개인정보처리방침에서 정한 경우를 제외하고는 회원의 개인정보가 제3자에게 공개 또는 제공되지 않도록 합니다.
3. 회사는 서비스 이용과 관련하여 회원으로부터 제기된 의견이나 불만이 정당하다고 인정할 경우에는 이를 처리하여야 합니다. 회원이 제기한 의견이나 불만사항에 대해서는 게시판을 활용하거나 전자우편 등을 통하여 회원에게 처리과정 및 결과를 전달합니다.
4. 회사는 계속적이고 안정적인 서비스의 제공을 위하여 서비스 개선을 하던 중 설비에 장애가 생기거나 데이터 등이 멸실된 때에는 천재지변, 비상사태, 현재의 기술로는 해결이 불가능한 결함 및 장애 등 부득이한 사유가 없는 한 지체 없이 이를 수리 또는 복구하도록 최선의 노력을 다합니다


<strong>제14조 이용자의 의무</strong>

