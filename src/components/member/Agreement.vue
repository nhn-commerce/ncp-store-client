import { mapState } from 'vuex'
import cookies from 'js-cookie'
import { getDateFormatForAlert } from '@/utils/dateUtils'
import config from '@/config'

export default {
  data () {
    return {
      shopChecked: false,
      shopAdChecked: false,
      personalChecked: false,
      fourteenOverChecked: false,
      running: false,
      mallName: config.mallName
    }
  },
  metaInfo: {
    title: '약관동의'
  },
  computed: {
    checkALL: {
      get () {
        return this.shopChecked && this.shopAdChecked && this.personalChecked && this.fourteenOverChecked
      },
      set (val) {
        this.shopChecked = val
        this.shopAdChecked = val
        this.personalChecked = val
        this.fourteenOverChecked = val
      }
    },
    ...mapState('profile', ['profile'])
  },
  methods: {
    go (nxturl) {
      if (opener) {
        if (!nxturl || nxturl === '' || nxturl === 'undefined') {
          opener.location.href = '/'
        } else {
          opener.location.href = nxturl
        }
        self.close()
      } else {
        if (!nxturl || nxturl === '' || nxturl === 'undefined') {
          location.href = '/'
        } else {
          location.href = nxturl
        }
      }
    },
    agreement () {
      if (this.running) {
        return
      } else {
        this.running = true
      }
      const nxturl = this.$store.state.route.query.nxturl
      if (!this.shopChecked) {
        alert(`${config.mallName} 약관에 동의 해 주세요.`)
        this.running = false
        return false
      }

      if (!this.personalChecked) {
        alert('개인정보 수집 및 이용에 동의 해 주세요.')
        this.running = false
        return false
      }

      if (!this.fourteenOverChecked) {
        alert("'만14세 이상입니다.' 에 동의해주세요. 만 14세 미만인 경우 회원 가입이 불가능합니다.")
        this.running = false
        return false
      }

      const _this = this
      if (this.shopAdChecked) {
        alert(`${config.mallName} 쇼핑혜택 알림에 동의하셨습니다. \r\n ${getDateFormatForAlert()}`)
      }
