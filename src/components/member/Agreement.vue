                })
              } else {
                _this.go(nxturl)
              }
            })
          } else {
            if (_this.shopAdChecked) {
              _this.$store.dispatch('profile/updateProfile', { smsAgreed: true, directMailAgreed: true }).then(() => {
                _this.go(nxturl)
              })
            } else {
              _this.go(nxturl)
            }
          }
        })
      })
    }
  }
}
